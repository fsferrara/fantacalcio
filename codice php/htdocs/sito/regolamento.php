<P ALIGN=JUSTIFY STYLE="margin-bottom: 0in; font-style: normal; font-weight: medium; text-decoration: none">
<FONT FACE="Lucidasans"><FONT SIZE=2>Il fantallenatore ha a
disposizione un budget iniziale di 200 &ldquo;fantacrediti&rdquo; con
i quali pu&ograve; comprare i giocatori. Nel momento in cui si d&agrave;
la conferma di ogni operazione di compravendita, i fantacrediti a
disposizione diminuiscono o aumentano in misura pari al costo del
calciatore in questione.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0in; font-style: normal; font-weight: medium; text-decoration: none">
<FONT FACE="Lucidasans"><FONT SIZE=2>Il valore di ciascun calciatore
(in fantacrediti) &egrave; stabilito prima dell'inizio del gioco e
pu&ograve; essere aggiornato durante il fantacampionato
dall&rsquo;amministratore. Potr&agrave; quindi salire o scendere
secondo il rendimento del giocatore.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0in; font-style: normal; font-weight: medium; text-decoration: none">
<FONT FACE="Lucidasans"><FONT SIZE=2>I fantacrediti eventualmente
risparmiati sono mantenuti dalla fantasquadra e possono essere
utilizzati successivamente per scambiare i propri giocatori con
giocatori liberi.</FONT></FONT></P>
