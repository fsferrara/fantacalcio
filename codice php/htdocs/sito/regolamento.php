<P ALIGN=JUSTIFY STYLE="margin-bottom: 0in; font-style: normal; font-weight: medium; text-decoration: none">
<FONT FACE="Lucidasans"><FONT SIZE=2>Ad ogni fantagiornata viene
assegnato un voto ad ogni giocatore, e ad ogni squadra gli vengono
assegnati un numero di punti pari alla somma dei voti ottenuti dai
propri giocatori titolari in quella giornata.</FONT></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0in; font-style: normal; font-weight: medium; text-decoration: none">
