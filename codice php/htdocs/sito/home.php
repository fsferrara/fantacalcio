Contenuto del progetto Lo svolgimento del progetto deve comprendere le seguenti fasi:<br>
<br>
- Definizione dello schema concettuale;<br>
- Definizione dello schema logico utilizzando il modello relazionale;<br>
- Definizione della base di dati utilizzando un DBMS relazionale;<br>
- Progettazione e realizzazione dell'interfaccia utente.<br>
<br>
Non ci sono preclusioni a priori nella scelta del DBMS relazionale o nell'eventuale scelta del linguaggio per la definizione dell'interfaccia (si consiglia comunque di comunicare in anticipo al docente la direzione della propria scelta).<br>
