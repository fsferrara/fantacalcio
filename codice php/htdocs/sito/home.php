<p align="center"><FONT size="+2">Documentazione del progetto</FONT><br></p>
<p align="left">
<br>
<br>
Per la valutazione del progetto va presentata al docente, entro i termini temporali fissati ad ogni appello in questo sito (e comunque in anticipo rispetto alla valutazione del corretto funzionamento dell'applicativo) una documentazione (cartacea) del lavoro.<br>
La documentazione deve contenere i seguenti elementi:<br>
<br>
Una descrizione testuale sintetica del problema;<br>
Schema E/R;<br>
Dizionario dei dati dello schema E/R;<br>
Eventuale ristrutturazione dello schema E/R;<br>
Schema relazionale;<br>
Descrizione della struttura dell'interfaccia utente.<br>
